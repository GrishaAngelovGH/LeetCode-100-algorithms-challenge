const { assertEquals } = require('../helpers')

const calcMean = values => {
  return parseFloat((values.reduce((a, b) => a + b) / values.length).toFixed(5))
}

const calcMedian = values => {
  const isEven = values.length % 2 === 0
  const middleIndex = Math.floor(values.length / 2)

  const median = isEven ?
    values.slice(middleIndex - 1, middleIndex + 1).reduce((a, b) => a + b) / 2 :
    values[middleIndex]

  return median
}

const calcMode = values => {
  const freq = {}

  values.forEach(v => {
    if (!freq[v]) {
      freq[v] = 0
    }

    freq[v]++
  })

  const mode = Number(Object.entries(freq).sort((a, b) => b[1] - a[1])[0][0])

  return mode
}

const sampleStats = (count) => {
  const values = []

  count.forEach((n, num) => {
    for (let i = 0; i < n; i++) {
      values.push(num)
    }
  })

  const min = Math.min(...values)
  const max = Math.max(...values)
  const mean = calcMean(values)
  const median = calcMedian(values)
  const mode = calcMode(values)

  return [min, max, mean, median, mode]
}

